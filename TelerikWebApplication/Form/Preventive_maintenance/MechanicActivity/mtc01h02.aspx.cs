        protected void RadGrid1_DeleteCommand(object sender, GridCommandEventArgs e)
        {
            var jobno = ((GridDataItem)e.Item).GetDataKeyValue("jobno");

            try
            {
                con.Open();
                cmd = new SqlCommand();
                cmd.CommandType = CommandType.Text;
                cmd.Connection = con;
                cmd.CommandText = "UPDATE tr_mcr_maH SET userid = @userid, lastupdate = GETDATE(), stEdit = '4' WHERE (jobno = @jobno)";
                cmd.Parameters.AddWithValue("@jobno", jobno);
                cmd.Parameters.AddWithValue("@userid", public_str.user_id);
                cmd.ExecuteNonQuery();
                con.Close();

                Label lblOk = new Label();
                lblOk.Text = "Data deleted successfully";
                lblOk.ForeColor = System.Drawing.Color.Teal;
                RadGrid1.Controls.Add(lblOk);
            }
            catch (Exception ex)
            {
                con.Close();
                Label lblError = new Label();
                lblError.Text = "Unable to delete data. Reason: " + ex.Message;
                lblError.ForeColor = System.Drawing.Color.Red;
                RadGrid1.Controls.Add(lblError);
                e.Canceled = true;
            }
        }

